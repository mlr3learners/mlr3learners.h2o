# nocov start
register_mlr3 = function(libname, pkgname) {
  # get mlr_learners dictionary from the mlr3 namespace
  x = utils::getFromNamespace("mlr_learners", ns = "mlr3")

  # add the learner to the dictionary
  x$add("classif.h2odeeplearning", LearnerClassifH2ODeeplearning)
  x$add("regr.h2odeeplearning", LearnerRegrH2ODeeplearning)
  x$add("classif.h2orandomforest", LearnerClassifH2ORandomForest)
  x$add("regr.h2orandomforest", LearnerRegrH2ORandomForest)
  x$add("classif.h2ogbm", LearnerClassifH2OGBM)
  x$add("regr.h2ogbm", LearnerRegrH2OGBM)
}

.onLoad = function(libname, pkgname) {
  register_mlr3()
  setHook(packageEvent("mlr3", "onLoad"), function(...) register_mlr3(),
          action = "append")
}

.onUnload = function(libpath) {
  event = packageEvent("mlr3", "onLoad")
  hooks = getHook(event)
  pkgname = vapply(hooks, function(x) environment(x)$pkgname, NA_character_)
  setHook(event, hooks[pkgname != "mlr3learners.h2o"],
          action = "replace")
}
# nocov end